package com.example.ignite.services.vehicles.common;

import java.util.Date;

/**
 * POJO used by Vehicle Service.
 */
public class Vehicle {
    private String name;

    private Date year;

    private Double price;

    public Vehicle(String name, Date year, Double price) {
        this.name = name;
        this.year = year;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getYear() {
        return year;
    }

    public void setYear(Date year) {
        this.year = year;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }
}
